public class Feedback {
    private String conteudo;
    private String ID;

    public String getConteudo()
    {
        return this.conteudo;
    }

    public String getID()
    {
        return this.ID;
    }

    public void setConteudo(String conteudo)
    {
        this.conteudo = conteudo;
    }

    public void setID(String ID)
    {
        this.ID = ID;
    }
}
